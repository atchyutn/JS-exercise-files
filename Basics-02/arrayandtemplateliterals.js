// we will write an example for an array and usage of template literals in JS.

// Declaring an array
let superHeros = ["IronMan", "SuperMan", "Thor"]

// calling an array
console.log(superHeros);

// finding the length of an array
console.log("Length of super heros:" + superHeros.length)

// another way of calling a variable inside a string
console.log(`Length of the super Heros: ${superHeros.length}`);